int	ft_atoi(char *num, int nb)
{
	int	i;
	int	result;

	i = 0;
	result = 0;
	while (i < nb)
	{
		result = (result * 10) + (num[i] - '0');
		i++;
	}
	if (result == 0)
		return (-1);
	return (result);
}
