char	*ft_strcat(char *dest, char *src)
{
	char	*p;
	int		i;

	p = dest;
	while (*p)
		p++;
	i = 0;
	while (src[i])
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (dest);
}
